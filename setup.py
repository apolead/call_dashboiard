# ANSI color codes for terminal output
class Colors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKCYAN = '\033[96m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'

def print_colored(text, color=Colors.ENDC):
    """Print colored text to terminal."""
    print(f"{color}{text}{Colors.ENDC}")

def print_header(text):
    """Print a header with styling."""
    print("\n" + "=" * 80)
    print_colored(f"ðŸŽ¯ {text}", Colors.BOLD + Colors.HEADER)
    print("=" * 80)

def print_step(step_num, total_steps, description):
    """Print a step with progress indicator."""
    progress = f"[{step_num}/{total_steps}]"
    print_colored(f"{progress} {description}...", Colors.OKBLUE)

def print_success(message):
    """Print success message."""
    print_colored(f"âœ… {message}", Colors.OKGREEN)

def print_warning(message):
    """Print warning message."""
    print_colored(f"âš ï¸  {message}", Colors.WARNING)

def print_error(message):
    """Print error message."""
    print_colored(f"âŒ {message}", Colors.FAIL)

def print_info(message):
    """Print info message."""
    print_colored(f"â„¹ï¸  {message}", Colors.OKCYAN)

def get_user_input(prompt, default=None, required=True):
    """Get user input with optional default value."""
    if default:
        full_prompt = f"{prompt} [{default}]: "
    else:
        full_prompt = f"{prompt}: "
    
    while True:
        value = input(full_prompt).strip()
        
        if value:
            return value
        elif default:
            return default
        elif not required:
            return ""
        else:
            print_error("This field is required. Please enter a value.")

